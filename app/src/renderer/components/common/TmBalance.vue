<template>
  <div class="header-balance">
    <div class="top">
      <div class="total-atoms top-section">
        <h3>Total {{ num.viewDenom(bondDenom) }}</h3>
        <h2 class="total-atoms__value">
          {{ totalAtomsDisplay }}
        </h2>
        <short-bech32 :address="session.address || ''" />
      </div>
      <div v-if="unbondedAtoms" class="unbonded-atoms top-section">
        <h3>Available {{ num.viewDenom(bondDenom) }}</h3>
        <h2>{{ unbondedAtoms }}</h2>
      </div>
      <div v-if="rewards" class="top-section">
        <h3>Pending Rewards</h3>
        <h2>{{ rewards }}</h2>
        <tm-btn
          id="withdraw-btn"
          class="withdraw-rewards"
          :value="connected ? 'Withdraw' : 'Connecting...'"
          :to="''"
          type="link"
          size="sm"
          @click.native="onWithdrawal"
        />
      </div>
    </div>
    <slot />
    <modal-withdraw-all-rewards ref="modalWithdrawAllRewards" />
  </div>
</template>
<script>
import num from "scripts/num"
import ShortBech32 from "common/ShortBech32"
import TmBtn from "common/TmBtn"
import ModalWithdrawAllRewards from "staking/ModalWithdrawAllRewards"
import { mapGetters } from "vuex"
export default {
  name: `tm-balance`,
  components: {
    ShortBech32,
    TmBtn,
    ModalWithdrawAllRewards
  },
  data() {
    return {
      num,
      lastUpdate: 0
    }
  },
  computed: {
    ...mapGetters([
      `connected`,
      `session`,
      `wallet`,
      `delegation`,
      `liquidAtoms`,
      `lastHeader`,
      `totalAtoms`,
      `bondDenom`,
      `distribution`
    ]),
    loaded() {
      return this.wallet.loaded && this.delegation.loaded
    },
    totalAtomsDisplay() {
      return this.loaded ? num.shortNumber(num.atoms(this.totalAtoms)) : `--`
    },
    unbondedAtoms() {
      return this.loaded
        ? this.num.shortNumber(this.num.atoms(this.liquidAtoms))
        : `--`
    },
    rewards() {
      if (!this.distribution.loaded) {
        return `--`
      }
      const rewards = this.distribution.totalRewards[this.bondDenom]
      return this.num.shortNumber(
        this.num.atoms(rewards && rewards > 10 ? rewards : 0)
      )
    }
  },
  watch: {
    lastHeader: {
      immediate: true,
      handler(newHeader) {
        const height = Number(newHeader.height)
        // run the update queries the first time and after every 10 blocks
        const waitedTenBlocks = height - this.lastUpdate >= 10
        if (
          this.session.signedIn &&
          (this.lastUpdate === 0 || waitedTenBlocks)
        ) {
          this.lastUpdate = height
          this.$store.dispatch(`getTotalRewards`)
          this.$store.dispatch(`queryWalletBalances`)
        }
      }
    }
  },
  methods: {
    onWithdrawal() {
      this.$refs.modalWithdrawAllRewards.open()
    }
  }
}
</script>
